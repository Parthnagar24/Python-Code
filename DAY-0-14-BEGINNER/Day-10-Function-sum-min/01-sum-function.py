# sum() function
#Adds up all numbers in a list, tuple, or any iterable (like a range). 

#sum(iterable, start)

# ex 1 : simple sum

numbers = [10, 20, 30]
print(sum(numbers)) #60


# ex 2 : sum with start

numbers = [10, 20, 30]
print(sum(numbers, 100)) #160

# ex 3 : using with range()


print(sum(range(1, 6)))  # 1+2+3+4+5


